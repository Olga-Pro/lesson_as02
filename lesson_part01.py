# Материал урока

import pandas as pd


data = {'Набор А' :[85, 90, 95, 100, 105], 'Набор Б' :[70, 80, 95, 110, 120],
    }

df = pd.DataFrame(data)

# Стандартное отклонение
stdA = df['Набор А'].std()
stdB = df['Набор Б'].std()

print(f"Стандаортное отклонение в наборе А {stdA}")
print(f"Стандаортное отклонение в наборе Б {stdB}")
